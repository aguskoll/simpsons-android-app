@Composable
fun AppTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colors = if (!useDarkTheme) {
        lightScheme
    } else {
        darkScheme
    }

    val dimensions = WindowType.getDimensions(context = LocalContext.current)

    val shapes = Shapes(
        extraSmall = RoundedCornerShape(ShapeRadius.extraSmall),
        small = RoundedCornerShape(ShapeRadius.small),
        medium = RoundedCornerShape(ShapeRadius.medium),
        large = RoundedCornerShape(ShapeRadius.large),
        extraLarge = RoundedCornerShape(ShapeRadius.extraLarge)
    )

    ProvideDimens(dimensions = dimensions) {
        MaterialTheme(
            colorScheme = colors,
            content = content,
            typography = Typography,
            shapes = shapes
        )
    }
}

@Composable
fun ProvideDimens(
    dimensions: Dimensions,
    content: @Composable () -> Unit
) {
    val dimensionSet = remember { dimensions }
    CompositionLocalProvider(LocalAppDimens provides dimensionSet, content = content)
}

private val LocalAppDimens = staticCompositionLocalOf {
    defaultDimensions
}

object AppData {
    val mainNavController: NavHostController
        @Composable
        @ReadOnlyComposable
        get() = LocalNavigation.current!!
}

internal val LocalNavigation = staticCompositionLocalOf<NavHostController?> { null }
